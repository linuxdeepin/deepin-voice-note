    /**
     * @brief 解密
     * @param 密文
     * @param 密钥
     * @return 需要处理的明文
     */
    QByteArray decode(const QByteArray &rawText, const QByteArray &key);

    /**
     * @brief 清除明文的扩展
     * @param decode处理后的密文
     * @return 明文
     */
    QByteArray removePadding(const QByteArray &rawText);

    /**
     * @brief 扩展密钥
     * @param key 密钥
     * @return 扩展后的密钥
     */
    QByteArray expandKey(const QByteArray &key);

    /**
     * @brief 根据当前值进行SBox转换
     * @param num 当前的值
     * @return 转换后的SBox值
     */
    quint8 getSBoxValue(quint8 num);

    /**
     * @brief 根据当前值进行逆SBox转换
     * @param num 当前的值
     * @return 原值
     */
    quint8 getInverseBoxValue(quint8 num);

    /**
     * @brief 密钥添加
     * @param round 当前轮次
     * @param expKey 扩展密钥
     */
    void addRoundKey(QByteArray *encData, const quint8 round, const QByteArray &expKey);

    /**
     * @brief 字节替换 使用SBox的值替换当前值
     */
    void subBytes(QByteArray *encData);

    /**
     * @brief 行位移
     */
    void shiftRows(QByteArray *encData);

    /**
     * @brief 列混合
     */
    void mixColumns(QByteArray *encData);

    /**
     * @brief 逆向列混合
     */
    void invMixColumns(QByteArray *encData);

    /**
     * @brief 逆向字节替换
     */
    void invSubBytes(QByteArray *encData);

    /**
     * @brief 逆向行位移
     */
    void invShiftRows(QByteArray *encData);

    /**
     * @brief 填充到指定大小
     * @param currSize 当前的大小
     * @param alignment 块的大小
     * @return 填充后的值
     */
    QByteArray getPadding(int currSize, int alignment);

    /**
     * @brief 加密过程
     * @param expKey 扩展密钥
     * @param in 处理后的明文
     * @return 密文
     */
    QByteArray encrypt(const QByteArray &expKey, const QByteArray &in);

    /**
     * @brief 解密过程
     * @param expKey 扩展密钥
     * @param in 处理后的密文
     * @return 半明文
     */
    QByteArray decrypt(const QByteArray &expKey, const QByteArray &in);


    /**
     * @brief 混合移位
     * @param x 进行混合移位的数据
     * @return 混合移位后的数据
     */
    static quint8 xTime(quint8 x);

    /**
     * @brief multiply 多混合移位
     * @param x 需要混合的数据
     * @param y 混合的位置
     * @return 混合后的数据
     */
    static quint8 multiply(quint8 x, quint8 y);

